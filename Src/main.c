/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
	static ConvertLSB LSB = {0};
	uint32_t Binary = 0;
	uint8_t Flag = 0;
	const char StartMessage[] = "Receiver Start\r\n";
	char DataBuffer[40];
	char OtherMessage[20];
  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_TIM14_Init();
  MX_TIM16_Init();
  MX_USART1_UART_Init();
  /* USER CODE BEGIN 2 */

#ifndef M_DELAY_USE
  /* Wait 200ms Timer */
  LL_TIM_ClearFlag_UPDATE(TIM14);	//INTflag clear
  LL_TIM_SetPrescaler(TIM14, (640-1));	//10us
  LL_TIM_SetAutoReload(TIM14, (20000-1));	//10us*20000 = 200ms
  LL_TIM_EnableCounter(TIM14);
#endif

  /* IR PulseCheck Timer */
  LL_TIM_SetPrescaler(TIM16, (64-1));	//1us
  LL_TIM_EnableCounter(TIM16);
#ifndef IR_USE_POLLING
  /* Buffer&Flag Set */
  RecieveIR_IT(TIM16, &Binary, &Flag, Init);
#endif

  HAL_UART_Transmit(&huart1, (uint8_t*)StartMessage, strlen(StartMessage), HAL_MAX_DELAY);
  /* Disable SystickINT */
  HAL_SuspendTick();

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
	  /* Check 200ms Pulse for EXTI */
	  LL_GPIO_TogglePin(pulse_GPIO_Port, pulse_Pin);

#ifdef IR_USE_POLLING
	  /* IR Decode Polling */
	  __disable_irq();
	  Binary = RecieveIR(TIM16);
	  if(Binary == IR_P_REPEAT)
	  {
		  strcpy(OtherMessage,"Repeat\r\n");
		  HAL_UART_Transmit(&huart1, (uint8_t*)OtherMessage, strlen(OtherMessage), HAL_MAX_DELAY);
	  }
	  else
	  {
		  BinaryToHex(&LSB, Binary);
		  sprintf(DataBuffer,"Add1:%X\r\nAdd2:%X\r\nData1:%X\r\nData2:%X\r\n",LSB.bit8,LSB.bit16,LSB.bit24,LSB.bit31);
		  HAL_UART_Transmit(&huart1, (uint8_t*)DataBuffer, strlen(DataBuffer), HAL_MAX_DELAY);

		  memset(&LSB,0,sizeof(LSB));
		  Binary = 0;
	  }
#else
	  /* IR Decode EXTI */
	  if(Flag != IR_DECODE_READY)
	  {
		  if(Flag == IR_NACK)
		  {
			  strcpy(OtherMessage,"Error\r\n");
			  HAL_UART_Transmit(&huart1, (uint8_t*)OtherMessage, strlen(OtherMessage), HAL_MAX_DELAY);
		  }
		  else if(Flag == IR_REPEAT)
		  {
			  strcpy(OtherMessage,"Repeat\r\n");
			  HAL_UART_Transmit(&huart1, (uint8_t*)OtherMessage, strlen(OtherMessage), HAL_MAX_DELAY);
		  }
		  else if(Flag == IR_ACK)
		  {
			  BinaryToHex(&LSB, Binary);
			  sprintf(DataBuffer,"Add1:%X\r\nAdd2:%X\r\nData1:%X\r\nData2:%X\r\n",LSB.bit8,LSB.bit16,LSB.bit24,LSB.bit31);
			  HAL_UART_Transmit(&huart1, (uint8_t*)DataBuffer, strlen(DataBuffer), HAL_MAX_DELAY);

			  memset(&LSB,0,sizeof(LSB));
			  Binary = 0;
		  }
		  Flag = IR_DECODE_READY;
	  }
#endif

#ifndef M_DELAY_USE
	  Sleep200ms(TIM14);
#else
	  LL_EXTI_EnableIT_0_31(LL_EXTI_LINE_3);
	  __NVIC_EnableIRQ(EXTI2_3_IRQn);
	  LL_mDelay(100);
	  LL_EXTI_EnableIT_0_31(LL_EXTI_LINE_3);
	  __NVIC_EnableIRQ(EXTI2_3_IRQn);
#endif

  }
  /* USER CODE END 3 */
}

